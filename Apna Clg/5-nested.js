let mark =29;
if(mark>=30){
    console.log('Pass');
    if(mark>=90){
        console.log('Grade :O');
    }
    else if(mark>=80){
        console.log('Grade :A');
    }
    else if(mark>=70){
        console.log('Grade :B');
    }
    else if(mark>=50){
        console.log('Grade :C');
    }
    else{
        console.log('Grade :D');
    }
}
else{
    console.log('Fail');
}
